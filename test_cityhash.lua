cityhash = require("cityhash")

-- helper function

-- hexlify binary string with optional leading '0', no-leading for
-- keeping same to result of printf("%x", <uint64_t>)
function hexlify(binstr, with_leading_zero)
   hexstr = ""
   idx_notzero = 0
   if not with_leading_zero then
      idx_notzero = 1
      while binstr:byte(idx_notzero) == 0 do
         idx_notzero = idx_notzero + 1
      end
      hexstr = string.format("%x", binstr:byte(idx_notzero))
   end

   for i=idx_notzero + 1, #binstr do
      hexstr = hexstr .. string.format("%02x", -- must with inner '0'
                                       binstr:byte(i))
   end

   return hexstr
end

k0 = "\xc3\xa5\xc8\x5c\x97\xcb\x31\x27" -- 0xc3a5c85c97cb3127ULL in bigendian
kSeed0 = "\x00\x00\x00\x00\x00\x12\xd6\x87" -- 1234567
kSeed1 = k0
kSeed128 = kSeed1 .. kSeed0
kTestSize = 300

function test(expected, data, offset, len)
   local ch = cityhash
   local subdata = data:sub(offset, offset + len - 1)
   u = ch.cityhash128(subdata)
   v = ch.cityhash128(subdata, kSeed128)
   assert(expected[1] == hexlify(ch.cityhash64(subdata)),
          "cityhash64")
   assert(expected[16] == hexlify(ch.cityhash32(subdata)),
          "cityhash32")
   assert(expected[2] == hexlify(ch.cityhash64(subdata, kSeed0)),
          "cityhash64withseed")
   assert(expected[3] == hexlify(ch.cityhash64(subdata, 
                                               kSeed0, kSeed1)),
          "cityhash64withseeds")
   assert(expected[3] == hexlify(ch.cityhash64(subdata, kSeed128)),
          "cityhash64withseed128")
   assert(expected[4] == hexlify(u:sub(9,16)), "cityhash128:low64")
   assert(expected[5] == hexlify(u:sub(1,8)), "cityhash128:high64")
   assert(expected[6] == hexlify(v:sub(9,16)), 
          "cityhash128withseed:low64")
   assert(expected[7] == hexlify(v:sub(1,8)),
          "cityhash128withseed:high64")
end

function dump(expected, data, offset, len)
   local subdata = data:sub(offset, offset + len - 1)
   u = cityhash.cityhash128(subdata)
   v = cityhash.cityhash128(subdata, kSeed128)
   return string.format('{"%s", "%s", "%s", "%s", "%s", "%s", "%s",\n"%s", "%s", "%s", "%s",\n"%s", "%s", "%s", "%s",\n"%s"}',
                        hexlify(cityhash.cityhash64(subdata)),
                        hexlify(cityhash.cityhash64(subdata, kSeed0)),
                        hexlify(cityhash.cityhash64(subdata, kSeed0, kSeed1)),
                        hexlify(u:sub(9,16)), -- low64
                        hexlify(u:sub(1,8)),  -- high64
                        hexlify(v:sub(9,16)), -- low64
                        hexlify(v:sub(1,8)), -- high64
                        "","","","", "","","","",
                        hexlify(cityhash.cityhash32(subdata)))
end

